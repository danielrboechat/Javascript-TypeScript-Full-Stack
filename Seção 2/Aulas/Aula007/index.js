// Constante = aquilo que não vai variar
// Usa-se CONS para declarar constantes
// São as mesmas regras das variáveis, com um único adendo: não pode mudar o conteúdo de uma constante, obviamente.

const nome = 'Daniel'

console.log(nome, 'nasceu em 2000')
console.log(`Em 2023 ${nome} conheceu Ludmyla`)
console.log(`${nome} casou-se com Ludmyla em 2025`)


